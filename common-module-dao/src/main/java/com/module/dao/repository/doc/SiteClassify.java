package com.module.dao.repository.doc;

import com.module.dao.entity.doc.Site;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by zxz on 2016/1/7.
 */
public class SiteClassify {
    private String classifiedName;
    private List<Site> siteList;

    public SiteClassify(String classfiedNameStr){
        classifiedName = classfiedNameStr;
        siteList = new ArrayList<Site>();
    }

    public String getClassifiedName() {
        return classifiedName;
    }

    public void setClassifiedName(String classifiedName) {
        this.classifiedName = classifiedName;
    }

    public List<Site> getSiteList() {
        return siteList;
    }

    public void setSiteList(List<Site> siteList) {
        this.siteList = siteList;
    }




}
