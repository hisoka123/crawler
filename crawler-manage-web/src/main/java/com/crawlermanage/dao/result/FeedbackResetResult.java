package com.crawlermanage.dao.result;

public class FeedbackResetResult {

	   private int id;
	   private String mes;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	@Override
	public String toString() {
		return "FeedbackResetResult [id=" + id + ", mes=" + mes + "]";
	}
	   
	   
	
}
