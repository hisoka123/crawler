package com.crawler.weixin.domain.json;

import java.io.Serializable;

/**
 * @author kingly
 *
 */
public class WeixinArticle implements Serializable {
	private static final long serialVersionUID = 1L;
	private String title;
	private String link;
	private String content;
	private String created_time;
	private String pre_image;
	private UserFeedJson from; //文章出处
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreated_time() {
		return created_time;
	}
	public void setCreated_time(String created_time) {
		this.created_time = created_time;
	}
	public String getPre_image() {
		return pre_image;
	}
	public void setPre_image(String pre_image) {
		this.pre_image = pre_image;
	}
	public UserFeedJson getFrom() {
		return from;
	}
	public void setFrom(UserFeedJson from) {
		this.from = from;
	}
	
}
