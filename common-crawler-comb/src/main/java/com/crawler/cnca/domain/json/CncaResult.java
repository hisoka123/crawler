package com.crawler.cnca.domain.json;

import java.io.Serializable;
import java.util.List;

public class CncaResult implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7128006930901004556L;

	private String actualTotal;
	// private String header;
	private String offset;
	private String pageNo;
	private String pageSize;
	// private String params;
	private List<CncaData> rows;
	private String success;
	private String total;
	private String totalPage;

	public String getActualTotal() {
		return actualTotal;
	}

	public void setActualTotal(String actualTotal) {
		this.actualTotal = actualTotal;
	}

	public String getOffset() {
		return offset;
	}

	public void setOffset(String offset) {
		this.offset = offset;
	}

	public String getPageNo() {
		return pageNo;
	}

	public void setPageNo(String pageNo) {
		this.pageNo = pageNo;
	}

	public String getPageSize() {
		return pageSize;
	}

	public void setPageSize(String pageSize) {
		this.pageSize = pageSize;
	}

	public List<CncaData> getRows() {
		return rows;
	}

	public void setRows(List<CncaData> rows) {
		this.rows = rows;
	}

	public String getSuccess() {
		return success;
	}

	public void setSuccess(String success) {
		this.success = success;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getTotalPage() {
		return totalPage;
	}

	public void setTotalPage(String totalPage) {
		this.totalPage = totalPage;
	}

	@Override
	public String toString() {
		return "CncaResult [actualTotal=" + actualTotal + ", offset=" + offset
				+ ", pageNo=" + pageNo + ", pageSize=" + pageSize + ",  rows="
				+ rows + ", success=" + success + ", total=" + total
				+ ", totalPage=" + totalPage + "]";
	}

}
