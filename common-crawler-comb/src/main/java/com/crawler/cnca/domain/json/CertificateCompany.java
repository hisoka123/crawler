package com.crawler.cnca.domain.json;

import java.io.Serializable;

public class CertificateCompany implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5657734892549114078L;	
	
	private String orgCode;
	private String orgName;
	private String orgDistrictName;
	private String checkC;
	private String randomCheckCode;

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public String getOrgDistrictName() {
		return orgDistrictName;
	}

	public void setOrgDistrictName(String orgDistrictName) {
		this.orgDistrictName = orgDistrictName;
	}

	public String getCheckC() {
		return checkC;
	}

	public void setCheckC(String checkC) {
		this.checkC = checkC;
	}

	public String getRandomCheckCode() {
		return randomCheckCode;
	}

	public void setRandomCheckCode(String randomCheckCode) {
		this.randomCheckCode = randomCheckCode;
	}

	@Override
	public String toString() {
		return "CertificateCompany [orgCode=" + orgCode + ", orgName="
				+ orgName + ", orgDistrictName=" + orgDistrictName
				+ ", checkC=" + checkC + ", randomCheckCode=" + randomCheckCode
				+ "]";
	}

}
