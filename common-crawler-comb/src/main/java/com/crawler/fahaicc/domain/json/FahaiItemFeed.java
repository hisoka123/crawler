package com.crawler.fahaicc.domain.json;

import java.io.Serializable;

/**
 * @author kingly
 * @date 2016年2月26日
 * 
 */
public class FahaiItemFeed implements Serializable {
	private static final long serialVersionUID = -7273008686104897752L;
	private String username;
	private String title;
	private String linkUrl;

	// 立案时间 开庭日期 审结日期 发布日期...
	private String filingDate;
	private String courtDate;
	private String conclusionDate;
	private String pubDate;

	// 机构
	private String authority;

	// 类别
	private String type;

	private String content;
	private String contentHtml;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getLinkUrl() {
		return linkUrl;
	}

	public void setLinkUrl(String linkUrl) {
		this.linkUrl = linkUrl;
	}

	public String getFilingDate() {
		return filingDate;
	}

	public void setFilingDate(String filingDate) {
		this.filingDate = filingDate;
	}

	public String getCourtDate() {
		return courtDate;
	}

	public void setCourtDate(String courtDate) {
		this.courtDate = courtDate;
	}

	public String getConclusionDate() {
		return conclusionDate;
	}

	public void setConclusionDate(String conclusionDate) {
		this.conclusionDate = conclusionDate;
	}

	public String getPubDate() {
		return pubDate;
	}

	public void setPubDate(String pubDate) {
		this.pubDate = pubDate;
	}

	public String getAuthority() {
		return authority;
	}

	public void setAuthority(String authority) {
		this.authority = authority;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getContentHtml() {
		return contentHtml;
	}

	public void setContentHtml(String contentHtml) {
		this.contentHtml = contentHtml;
	}

	@Override
	public String toString() {
		return "FahaiItemFeed [username=" + username + ", title=" + title
				+ ", content=" + content + ", contentHtml=" + contentHtml
				+ ", filingDate=" + filingDate + ", courtDate=" + courtDate
				+ ", conclusionDate=" + conclusionDate + ", pubDate=" + pubDate
				+ ", authority=" + authority + ", type=" + type + ", linkUrl="
				+ linkUrl + "]";
	}
}
