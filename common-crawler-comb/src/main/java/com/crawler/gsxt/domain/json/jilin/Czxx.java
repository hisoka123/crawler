package com.crawler.gsxt.domain.json.jilin;

/**
 * 企业公示信息->股东及出资信息->股东及出资信息
 */
public class Czxx {

	private String inv;// 股东
	private String lisubconam;// 认缴额（万元）
	private String liacconam;// 实缴额（万元）

	public String getInv() {
		return inv;
	}

	public void setInv(String inv) {
		this.inv = inv;
	}

	public String getLisubconam() {
		return lisubconam;
	}

	public void setLisubconam(String lisubconam) {
		this.lisubconam = lisubconam;
	}

	public String getLiacconam() {
		return liacconam;
	}

	public void setLiacconam(String liacconam) {
		this.liacconam = liacconam;
	}

	@Override
	public String toString() {
		return "Czxx [inv=" + inv + ", lisubconam=" + lisubconam
				+ ", liacconam=" + liacconam + "]";
	}

}
