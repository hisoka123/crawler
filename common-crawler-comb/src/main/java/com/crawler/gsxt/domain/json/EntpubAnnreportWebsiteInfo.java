public class EntpubAnnreportWebsiteInfo implements Serializable { //QygsQynbWzhwdInfo
	private static final long serialVersionUID = -2686574133346563564L;
	
	//类型
	private String type;
	//名称
	private String name;
	//网址
	private String website;
	//html内容
	private String html;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public String getHtml() {
		return html;
	}
	public void setHtml(String html) {
		this.html = html;
	}
	@Override
	public String toString() {
		return "EntpubAnnreportWebsiteInfo [type=" + type + ", name=" + name
				+ ", website=" + website + ", html=" + html + "]";
	}

}
