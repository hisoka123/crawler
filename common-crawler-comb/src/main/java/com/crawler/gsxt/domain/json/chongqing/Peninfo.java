package com.crawler.gsxt.domain.json.chongqing;


import java.util.ArrayList;
import java.util.List;

public class Peninfo {

    private String entNo;
    private List<JPeninfo> jPeninfos = new ArrayList<>();

    public Peninfo() {    }

    public Peninfo(String entNo, List<JPeninfo> jPeninfos) {
        this.entNo = entNo;
        this.jPeninfos = jPeninfos;
    }

    public String getEntNo() {
        return entNo;
    }

    public void setEntNo(String entNo) {
        this.entNo = entNo;
    }

    public List<JPeninfo> getjPeninfos() {
        return jPeninfos;
    }

    public void setjPeninfos(List<JPeninfo> jPeninfos) {
        this.jPeninfos = jPeninfos;
    }

    @Override
    public String toString() {
        return "Peninfo{" +
                "entNo='" + entNo + '\'' +
                ", jPeninfos=" + jPeninfos +
                '}';
    }
}
