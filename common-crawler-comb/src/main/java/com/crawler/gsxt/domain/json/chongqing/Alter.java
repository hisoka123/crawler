package com.crawler.gsxt.domain.json.chongqing;



public class Alter {

    private String oid;
    private String pripid;
    private String altitemno;
    private String altitem;
    private String altbe;
    private String altaf;
    private String altdate;

    public Alter() {    }

    public Alter(String oid, String pripid, String altitemno, String altitem,
                 String altbe, String altaf, String altdate) {
        this.oid = oid;
        this.pripid = pripid;
        this.altitemno = altitemno;
        this.altitem = altitem;
        this.altbe = altbe;
        this.altaf = altaf;
        this.altdate = altdate;
    }

    public String getOid() {
        return oid;
    }

    public void setOid(String oid) {
        this.oid = oid;
    }

    public String getPripid() {
        return pripid;
    }

    public void setPripid(String pripid) {
        this.pripid = pripid;
    }

    public String getAltitemno() {
        return altitemno;
    }

    public void setAltitemno(String altitemno) {
        this.altitemno = altitemno;
    }

    public String getAltitem() {
        return altitem;
    }

    public void setAltitem(String altitem) {
        this.altitem = altitem;
    }

    public String getAltbe() {
        return altbe;
    }

    public void setAltbe(String altbe) {
        this.altbe = altbe;
    }

    public String getAltaf() {
        return altaf;
    }

    public void setAltaf(String altaf) {
        this.altaf = altaf;
    }

    public String getAltdate() {
        return altdate;
    }

    public void setAltdate(String altdate) {
        this.altdate = altdate;
    }

    @Override
    public String toString() {
        return "Alter{" +
                "oid='" + oid + '\'' +
                ", pripid='" + pripid + '\'' +
                ", altitemno='" + altitemno + '\'' +
                ", altitem='" + altitem + '\'' +
                ", altbe='" + altbe + '\'' +
                ", altaf='" + altaf + '\'' +
                ", altdate='" + altdate + '\'' +
                '}';
    }
}
