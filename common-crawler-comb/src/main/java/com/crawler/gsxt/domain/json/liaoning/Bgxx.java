package com.crawler.gsxt.domain.json.liaoning;

/**
 * 工商公示信息->登记信息->变更信息
 */
public class Bgxx {

	private String altitemName;// 变更事项
	private String altbe;// 变更前内容
	private String altaf;// 变更后内容
	private String altdate;// 变更日期

	public String getAltitemName() {
		return altitemName;
	}

	public void setAltitemName(String altitemName) {
		this.altitemName = altitemName;
	}

	public String getAltbe() {
		return altbe;
	}

	public void setAltbe(String altbe) {
		this.altbe = altbe;
	}

	public String getAltaf() {
		return altaf;
	}

	public void setAltaf(String altaf) {
		this.altaf = altaf;
	}

	public String getAltdate() {
		return altdate;
	}

	public void setAltdate(String altdate) {
		this.altdate = altdate;
	}

	@Override
	public String toString() {
		return "Bgxx [altitemName=" + altitemName + ", altbe=" + altbe
				+ ", altaf=" + altaf + ", altdate=" + altdate + "]";
	}

}
