package com.crawler.linkedin.domain.json;

import java.io.Serializable;

/**
 * @author kingly
 *
 */
public class Organization implements Serializable {
	private static final long serialVersionUID = 1L;
	private String name;
	private String cate; //机构类型
	private String role; //参与身份
	private String position; //担任职务
	private String org_date; //开始参与时间
	private String desc;
	private String desc_html;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCate() {
		return cate;
	}
	public void setCate(String cate) {
		this.cate = cate;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getOrg_date() {
		return org_date;
	}
	public void setOrg_date(String org_date) {
		this.org_date = org_date;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getDesc_html() {
		return desc_html;
	}
	public void setDesc_html(String desc_html) {
		this.desc_html = desc_html;
	}
	
	@Override
	public String toString() {
		return "Organization [name=" + name + ", cate=" + cate + ", role="
				+ role + ", position=" + position + ", org_date=" + org_date
				+ ", desc=" + desc + ", desc_html=" + desc_html + "]";
	}
	
}
