package com.crawler.zhengxin.domain.json;

import java.io.Serializable;
/**

* 浙法网曝光台信息查询bean

* @author dyx

*/
public class ZhengxinJson implements Serializable {

	private static final long serialVersionUID = 1L;
	private String date;//发布时间
	private String company;//发布单位
	private String content;//档案内容
	private String type;//（行政奖罚信息，行业协会(社会组织)评价信息）
	private String title;//档案标题
	private String link;//标题连接
	private String property;//档案性质
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getProperty() {
		return property;
	}
	public void setProperty(String property) {
		this.property = property;
	}
	


}
