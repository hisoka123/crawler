package com.crawler.sxjlcxpt.domain.json;

import java.io.Serializable;
import java.util.List;
import java.util.Map;

public class UserFeedJson implements Serializable{
	private String tipMessage;
	
	private List<String> thead;
	
	private List<Map<String, String>> tbody;

	public String getTipMessage() {
		return tipMessage;
	}

	public void setTipMessage(String tipMessage) {
		this.tipMessage = tipMessage;
	}

	public List<String> getThead() {
		return thead;
	}

	public void setThead(List<String> thead) {
		this.thead = thead;
	}

	public List<Map<String, String>> getTbody() {
		return tbody;
	}

	public void setTbody(List<Map<String, String>> tbody) {
		this.tbody = tbody;
	}

}
