package com.crawler.pbccrc.domain.json;

import java.io.Serializable;

/**
 * @author kingly
 * @date 2016年1月6日
 * 
 */
public class ReportBase implements Serializable {
	private static final long serialVersionUID = -4224879384904937659L;
	private String reportId;
	private String queryTime;
	private String reportTime;
	private String realname;
	private String certificateType;
	private String certificateNum;
	private String marriageStatus; //0:未婚   1:已婚  -1:未知
	
	public String getReportId() {
		return reportId;
	}
	public void setReportId(String reportId) {
		this.reportId = reportId;
	}
	public String getQueryTime() {
		return queryTime;
	}
	public void setQueryTime(String queryTime) {
		this.queryTime = queryTime;
	}
	public String getReportTime() {
		return reportTime;
	}
	public void setReportTime(String reportTime) {
		this.reportTime = reportTime;
	}
	public String getRealname() {
		return realname;
	}
	public void setRealname(String realname) {
		this.realname = realname;
	}
	public String getCertificateType() {
		return certificateType;
	}
	public void setCertificateType(String certificateType) {
		this.certificateType = certificateType;
	}
	public String getCertificateNum() {
		return certificateNum;
	}
	public void setCertificateNum(String certificateNum) {
		this.certificateNum = certificateNum;
	}
	public String getMarriageStatus() {
		return marriageStatus;
	}
	public void setMarriageStatus(String marriageStatus) {
		this.marriageStatus = marriageStatus;
	}
	
	@Override
	public String toString() {
		return "ReportBase [reportId=" + reportId + ", queryTime=" + queryTime
				+ ", reportTime=" + reportTime + ", realname=" + realname
				+ ", certificateType=" + certificateType + ", certificateNum="
				+ certificateNum + ", marriageStatus=" + marriageStatus + "]";
	}
}
