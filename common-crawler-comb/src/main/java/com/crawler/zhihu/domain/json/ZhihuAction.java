package com.crawler.zhihu.domain.json;

import java.io.Serializable;

/**
 * @author kingly
 * 
 */
public class ZhihuAction implements Serializable {
	private static final long serialVersionUID = 1L;
	private String operation;
	private String operate_time;
	private ZhihuAnswer answer;
	private ZhihuArticle article;
	
	public String getOperation() {
		return operation;
	}

	public void setOperation(String operation) {
		this.operation = operation;
	}

	public String getOperate_time() {
		return operate_time;
	}

	public void setOperate_time(String operate_time) {
		this.operate_time = operate_time;
	}

	public ZhihuAnswer getAnswer() {
		return answer;
	}

	public void setAnswer(ZhihuAnswer answer) {
		this.answer = answer;
	}

	public ZhihuArticle getArticle() {
		return article;
	}

	public void setArticle(ZhihuArticle article) {
		this.article = article;
	}

	@Override
	public String toString() {
		return "ZhihuAction [operation=" + operation + ", operate_time="
				+ operate_time + ", answer=" + answer + ", article=" + article
				+ "]";
	}
}
