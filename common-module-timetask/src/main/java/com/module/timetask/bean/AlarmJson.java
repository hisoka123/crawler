package com.module.timetask.bean;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

public class AlarmJson implements Serializable {
	

	/**
	 * 
	 */
	private static final long serialVersionUID = -5181610290305616608L;
	
	private String name;
	
	private Date updatime = new Date();
	
	private List<String> regionIds;
	
	private List<String> regionChiNams;

	private List<String> emails;

	public List<String> getRegionIds() {
		return regionIds;
	}
	
	public void setRegionIds(List<String> regionIds) {
		this.regionIds = regionIds;
	}
	
	public List<String> getRegionChiNams() {
		return regionChiNams;
	}
	
	public void setRegionChiNams(List<String> regionChiNams) {
		this.regionChiNams = regionChiNams;
	}


		public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getUpdatime() {
		return updatime;
	}

	public void setUpdatime(Date updatime) {
		this.updatime = updatime;
	}

	public List<String> getEmails() {
		return emails;
	}

	public void setEmails(List<String> emails) {
		this.emails = emails;
	}

	
}
